@Composable
fun CircularProgressBar(
    number: Float = 70f,
    numberStyle: TextStyle = TextStyle(
        fontFamily = FontFamily.Default,
        fontWeight = FontWeight.Bold,
        fontSize = 28.sp
    ),
    size: Dp = 180.dp,
    indicatorThickness: Dp = 28.dp,
    animationDuration: Int = 1000,
    animationDelay: Int = 0,
    foregroundIndicatorColor: Color = colorResource(id = R.color.purple_1),
    backgroundIndicatorColor: Color = Color.LightGray.copy(alpha = 0.3f),
    modifier: Modifier = Modifier.fillMaxSize(1f)
    ) {
    var numberR by rememberSaveable {
        mutableStateOf(0f)
    }

    val animateNumber = animateFloatAsState(
        targetValue = numberR,
        animationSpec = tween(
            durationMillis = animationDuration,
            delayMillis = animationDelay
        )
    )

    LaunchedEffect(Unit) {
        numberR = number
    }
Column(
    verticalArrangement = Arrangement.Center,
    horizontalAlignment = Alignment.CenterHorizontally,
    modifier = modifier
        .padding(vertical = 16.dp)
    ) {
    Box(
        contentAlignment = Alignment.Center,
        modifier = Modifier
            .size(size = size)
    ) {
        Canvas(
            modifier = Modifier
                .size(size = size)
        ) {
            drawCircle(
                color = backgroundIndicatorColor,
                radius = size.toPx() / 2,
                style = Stroke(width = indicatorThickness.toPx(), cap = StrokeCap.Round)
            )
            val sweepAngle = (animateNumber.value / 100) * 360
            drawArc(
                color = foregroundIndicatorColor,
                startAngle = -90f,
                sweepAngle = sweepAngle,
                useCenter = false,
                style = Stroke(indicatorThickness.toPx(), cap = StrokeCap.Round)
            )
        }
        Text(
            text = (animateNumber.value).toInt().toString(),
            style = numberStyle
        )
    }
    Spacer(modifier = Modifier.height(32.dp))
    ButtonProgressbar {
        numberR = (1..100).random().toFloat()
    }
    }
}
